package user;

public class Ticket {
	private String screen_id;
	private String seat_num;
	private String customer_id;
	private String cinema_name;
	private String showing_date;
	private String showing_time;
	private String pay_how;
	private String gross;
	private String checkV;
	public String getScreen_id() {
		return screen_id;
	}
	public void setScreen_id(String screen_id) {
		this.screen_id = screen_id;
	}
	public String getSeat_num() {
		return seat_num;
	}
	public void setSeat_num(String seat_num) {
		this.seat_num = seat_num;
	}
	public String getCustomer_id() {
		return customer_id;
	}
	public void setCustomer_id(String customer_id) {
		this.customer_id = customer_id;
	}
	public String getCinema_name() {
		return cinema_name;
	}
	public void setCinema_name(String cinema_name) {
		this.cinema_name = cinema_name;
	}
	public String getShowing_date() {
		return showing_date;
	}
	public void setShowing_date(String showing_date) {
		this.showing_date = showing_date;
	}
	public String getShowing_time() {
		return showing_time;
	}
	public void setShowing_time(String showing_time) {
		this.showing_time = showing_time;
	}
	public String getPay_how() {
		return pay_how;
	}
	public void setPay_how(String pay_how) {
		this.pay_how = pay_how;
	}
	public String getGross() {
		return gross;
	}
	public void setGross(String gross) {
		this.gross = gross;
	}
	public String getCheckV() {
		return checkV;
	}
	public void setCheckV(String checkV) {
		this.checkV = checkV;
	}


}
